#include <bits/stdc++.h>

/* https://www.codechef.com/problems/CCOOK */

using namespace std;


int main(){
    int N, a[5], tot;

    cin >> N;
    for(int i=0; i<=N; ++i){
        cin >> a[i];
        for (int j = 5; j<=5; ++j){
            tot = 0;
            tot += a[i];
        }
    }

    return 0;
}
